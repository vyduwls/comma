package com.comma.albaman.vo;

public class Attendance {
	String date;
	String mid;
	String name;
	String position;
	String preOnWork;
	String preOffWork;
	String onWork;
	String offWork;
	String onWorkState;
	String offWorkState;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getPreOnWork() {
		return preOnWork;
	}
	public void setPreOnWork(String preOnWork) {
		this.preOnWork = preOnWork;
	}
	public String getPreOffWork() {
		return preOffWork;
	}
	public void setPreOffWork(String preOffWork) {
		this.preOffWork = preOffWork;
	}
	public String getOnWork() {
		return onWork;
	}
	public void setOnWork(String onWork) {
		this.onWork = onWork;
	}
	public String getOffWork() {
		return offWork;
	}
	public void setOffWork(String offWork) {
		this.offWork = offWork;
	}
	public String getOnWorkState() {
		return onWorkState;
	}
	public void setOnWorkState(String onWorkState) {
		this.onWorkState = onWorkState;
	}
	public String getOffWorkState() {
		return offWorkState;
	}
	public void setOffWorkState(String offWorkState) {
		this.offWorkState = offWorkState;
	}
}
