package com.comma.albaman.vo;

public class Schedule {
	
		private String sseq;
		private String preOnWork;
		private String preOffWork;
		private String onWork;
		private String offWork;
		private String mid;
		
		public String getSseq() {
			return sseq;
		}
		public void setSseq(String sseq) {
			this.sseq = sseq;
		}
	
		public String getPreOnWork() {
			return preOnWork;
		}
		public void setPreOnWork(String preOnWork) {
			this.preOnWork = preOnWork;
		}
		public String getPreOffWork() {
			return preOffWork;
		}
		public void setPreOffWork(String preOffWork) {
			this.preOffWork = preOffWork;
		}
		public String getOnWork() {
			return onWork;
		}
		public void setOnWork(String onWork) {
			this.onWork = onWork;
		}
		public String getOffWork() {
			return offWork;
		}
		public void setOffWork(String offWork) {
			this.offWork = offWork;
		}
		public String getMid() {
			return mid;
		}
		public void setMid(String mid) {
			this.mid = mid;
		}
		
	
}
